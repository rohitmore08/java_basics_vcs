public class example1 

{
	public static void main(String[] args) 
	{
		
		for (int i=0; i<=1; i++) //outer for loop --> for rows
		{
			
			for	(int j=0; j<=2; j++)//inner for loop --> for column
			{
				System.out.print("*");
			
			}
		System.out.println("");
		}
		
	}

}
