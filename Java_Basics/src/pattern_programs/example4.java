public class example4 

{
	public static void main(String[] args) 
	{
		int star=1;
		
		for (int i=0; i<=4; i++)
		{
			for( int j=0; j<star; j++)
			{
				System.out.print("*");
			}
			System.out.println();
			star++; 
		}

		
		
	}


}
